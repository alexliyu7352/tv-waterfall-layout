package com.msisuzney.tv_waterfallayout.bean;

public abstract class Collection {
    //行的宽高
    private int width, height;

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}
